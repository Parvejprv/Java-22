
package string.pkgclass.and.printing;
import java.util.*;
/**
 *
 * @author HP
 */
public class StringClassAndPrinting {

    public static void main(String[] args) {
        
        int a = 10, y = 20;
        float  b = 12.55f;
        char c = 'A';
        String str = "Hello Learner!";
        
        System.out.println(a);          // Integer
        System.out.println(b);          // Floar
        System.out.println(c);          // Character
        System.out.println(str);        // String 
        System.out.println("Sum of " + a + " & " + y + " is: " + (a+y));        // Concatination           // Expression or sum
        
    }
    
}
